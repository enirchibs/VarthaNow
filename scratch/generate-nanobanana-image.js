import { createClient } from "@supabase/supabase-js";
import Parser from "rss-parser";
import * as fs from "fs";

// Load .env
try {
  const envText = fs.readFileSync(".env", "utf8");
  for (const line of envText.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx < 0) continue;
    const key = trimmed.slice(0, idx).trim();
    const value = trimmed.slice(idx + 1).trim().replace(/^['"]|['"]$/g, "");
    if (key && value) process.env[key] = value;
  }
} catch {}

const supabaseUrl = process.env.VITE_SUPABASE_URL;
const serviceRole = process.env.SUPABASE_SERVICE_ROLE_KEY;
const geminiKey = process.env.GEMINI_API_KEY;

const supabase = createClient(supabaseUrl, serviceRole, { auth: { persistSession: false } });
const parser = new Parser();

async function runNanoBananaGeneration() {
  console.log("============================================================");
  console.log("🚀 STARTING NANO BANANA 2 GENERATION (1024x1024, DYNAMIC, LOW)");
  console.log("============================================================");

  // 1. Fetch upcoming article from RSS (fourth item!)
  console.log("\n1. Fetching latest Andhra Pradesh RSS feeds...");
  const feedUrl = "https://news.google.com/rss/search?q=andhra+pradesh+latest+breaking&hl=te&gl=IN&ceid=IN:te";
  const rss = await parser.parseURL(feedUrl);
  
  const item = rss.items[3] || rss.items[0];
  if (!item || !item.title) {
    console.error("No news articles found in RSS feed.");
    return;
  }
  console.log(`   ✓ Found news article: "${item.title}"`);

  // 2. Try Google's Nano Banana 2 API, fall back to Pollinations AI
  console.log("\n2. Dispatching generation to Google's Nano Banana 2 API...");
  const cleanTitle = item.title.split(" - ")[0].trim();
  const imagePrompt = `Create a professional Telugu news website banner image. Realistic editorial news photograph representing: ${cleanTitle}. 1024x1024 square, dynamic style, low contrast. Place high-quality, photorealistic journalism photography. Natural lighting and colors, suitable for news, high quality, no text, no logo.`;

  let publicImageUrl = null;
  try {
    const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/gemini-3.1-flash-image-preview:generateContent?key=${geminiKey}`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        contents: [
          {
            role: "user",
            parts: [{ text: imagePrompt }]
          }
        ],
        generationConfig: {
          responseModalities: ["TEXT", "IMAGE"]
        }
      })
    });

    if (response.ok) {
      const data = await response.json();
      const part = data.candidates?.[0]?.content?.parts?.find((p) => p.inlineData);
      const base64 = part?.inlineData?.data;
      if (base64) {
        console.log(`   ✓ Image generated by Nano Banana 2 successfully.`);
        publicImageUrl = await uploadToSupabase(Buffer.from(base64, "base64"), "nanobanana.jpg");
      }
    } else {
      console.warn(`   ⚠ Nano Banana 2 returned status: ${response.status}. Triggering Pollinations fallback...`);
    }
  } catch (error) {
    console.warn("   ⚠ Nano Banana 2 API failed. Triggering Pollinations fallback:", error);
  }

  // Fallback Generation
  if (!publicImageUrl) {
    console.log("\n⏳ Fetching visual banner from Pollinations AI (Nano Banana 2 fallback)...");
    const fallbackUrl = `https://image.pollinations.ai/prompt/${encodeURIComponent(imagePrompt)}?width=1024&height=1024&nologo=true&private=true&t=${Date.now()}`;
    const imgRes = await fetch(fallbackUrl);
    if (!imgRes.ok) {
      console.error("Failed to generate image via fallback Pollinations API.");
      return;
    }
    const arrayBuffer = await imgRes.arrayBuffer();
    publicImageUrl = await uploadToSupabase(Buffer.from(arrayBuffer), "nanobanana-fallback.jpg");
  }

  if (!publicImageUrl) {
    console.error("Failed to upload generated cover banner.");
    return;
  }

  // 4. Insert Post into Database
  console.log("\n4. Inserting article into database...");
  const slug = `nanobanana-news-${Date.now()}`;
  const { error: insertError } = await supabase.from("blog_posts").insert({
    slug,
    title: cleanTitle,
    excerpt: "తాజా వార్తలు",
    content: `## ${cleanTitle}\n\nవివరాలు త్వరలోనే నవీకరించబడతాయి.`,
    category: "andhra-pradesh",
    tags: ["andhra-pradesh", "politics", "nanobanana"],
    meta_title: cleanTitle,
    meta_description: "తాజా వార్తలు",
    og_image: publicImageUrl,
    author_name: "VarthaNow Auto Desk",
    language: "te",
    published: true,
    featured: false,
    reading_time_min: 3,
    published_at: new Date().toISOString()
  });

  if (insertError) {
    console.error("   ✗ Database insertion failed:", insertError.message);
  } else {
    console.log(`\n🎉 SUCCESS! Generated post successfully:`);
    console.log(`👉 COVER IMAGE URL: ${publicImageUrl}`);
  }
}

async function uploadToSupabase(buffer, baseName) {
  const fileName = `${Date.now()}-${baseName}`;
  const filePath = `article-images/${fileName}`;

  const { error: uploadError } = await supabase.storage
    .from("news-images")
    .upload(filePath, buffer, {
      contentType: "image/jpeg",
      cacheControl: "3600",
      upsert: false
    });

  if (uploadError) {
    console.error("   ✗ Supabase upload error:", uploadError.message);
    return null;
  }

  const { data: urlData } = supabase.storage.from("news-images").getPublicUrl(filePath);
  return urlData.publicUrl;
}

runNanoBananaGeneration().catch(console.error);
