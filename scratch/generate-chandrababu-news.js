import { createClient } from "@supabase/supabase-js";
import * as fs from "fs";

// Load .env
try {
  const envText = fs.readFileSync(".env", "utf8");
  for (const line of envText.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const idx = trimmed.indexOf("=");
    if (idx < 0) continue;
    const key = trimmed.slice(0, idx).trim();
    const value = trimmed.slice(idx + 1).trim().replace(/^['"]|['"]$/g, "");
    if (key && value) process.env[key] = value;
  }
} catch {}

const supabaseUrl = process.env.VITE_SUPABASE_URL;
const serviceRole = process.env.SUPABASE_SERVICE_ROLE_KEY;
const leonardoKey = process.env.LEONARDO_API_KEY || "caa189c3-0676-41f4-9095-11c7eac9ca28";

const supabase = createClient(supabaseUrl, serviceRole, { auth: { persistSession: false } });

async function runGenerations() {
  console.log("============================================================");
  console.log("🚀 STARTING CHANDRABABU NEWS IMAGE GENERATION VIA GPT-IMAGE-2");
  console.log("============================================================");

  const headline = "రాష్ట్రాలు వేరైనా తెలుగు జాతి ఒక్కటే.. హైదరాబాద్, అమరావతి ప్రపంచంలోనే బెస్ట్ సిటీస్ కావాలి.";
  const summary = "హైదరాబాద్, అమరావతి నగరాలను ప్రపంచ స్థాయి నగరాలుగా తీర్చిదిద్దేందుకు కృషి చేస్తానని ఏపీ సీఎం నారా చంద్రబాబు నాయుడు వెల్లడించారు. రెండు రాష్ట్రాలు వేరైనా, తెలుగు జాతి అంతా ఒక్కటేనని పేర్కొన్నారు.";
  
  const imagePrompt = `Create a professional digital news portal card.
At the top-left, the Telugu headline is displayed prominently in clean, bold, large typography: 'రాష్ట్రాలు వేరైనా తెలుగు జాతి ఒక్కటే.. హైదరాబాద్, అమరావతి ప్రపంచంలోనే బెస్ట్ సిటీస్ కావాలి.'.
On the right, a highly realistic, professional editorial portrait photograph of Chandrababu Naidu, the veteran Indian politician, speaking at a podium with microphones in front of him.
At the bottom, place two side-by-side rectangular panels: the left panel shows the Charminar in Hyderabad, and the right panel shows the modern architecture/skyline of Amaravati.
At the very bottom of the card, display a dark horizontal banner with Telugu quote text: 'మన బలం.. మన ఐక్యత.. మన భవిష్యత్ తెలుగు తేజం ప్రపంచానికి దిక్సూచి కావాలి.'.
Clean editorial layout, realistic news media graphic, 1024x1024 square, dynamic style, low contrast.`;

  const negativePrompt = "cartoon, anime, painting, illustration, blurry text, distorted text, watermark, logo, low quality, unrealistic faces, AI artifacts, cluttered layout.";

  // Call the generations API with modelId "gpt-image-2", DYNAMIC preset, and 1024x1024 square dimensions
  const response = await fetch("https://cloud.leonardo.ai/api/rest/v2/generations", {
    method: "POST",
    headers: {
      "Authorization": `Bearer ${leonardoKey}`,
      "Content-Type": "application/json",
      "Accept": "application/json"
    },
    body: JSON.stringify({
      model: "gpt-image-2", // GPT Image 2
      public: false,
      parameters: {
        prompt: imagePrompt,
        width: 1024,
        height: 1024,
        presetStyle: "DYNAMIC",  // Dynamic preset
        contrast: 3.0,          // Low contrast as specified
        seed: 242008,           // Fixed seed as requested
        negative_prompt: negativePrompt
      }
    })
  });

  if (!response.ok) {
    const errText = await response.text();
    console.error(`   ✗ Leonardo AI initiation failed:`, response.statusText, errText);
    return;
  }

  const data = await response.json();
  const generationId = data.generate?.generationId || data.sdGenerationJob?.generationId || data.generation?.id;
  if (!generationId) {
    console.error("   ✗ No generationId returned.");
    return;
  }
  console.log(`   ✓ Generation queued. ID: ${generationId}. Polling...`);

  // Poll Leonardo
  let attempts = 0;
  let imageUrl = null;
  while (attempts < 20) {
    await new Promise(resolve => setTimeout(resolve, 4000));
    attempts++;
    
    const pollRes = await fetch(`https://cloud.leonardo.ai/api/rest/v1/generations/${generationId}`, {
      headers: {
        "Authorization": `Bearer ${leonardoKey}`,
        "Accept": "application/json"
      }
    });
    
    if (!pollRes.ok) continue;
    const pollData = await pollRes.json();
    const generation = pollData.generations_by_pk;
    
    if (generation) {
      console.log(`   [Attempt ${attempts}/20] Status: ${generation.status}`);
      if (generation.status === "COMPLETE") {
        imageUrl = generation.generated_images?.[0]?.url || null;
        break;
      } else if (generation.status === "FAILED") {
        console.error("   ✗ Image generation failed.");
        return;
      }
    }
  }

  if (!imageUrl) {
    console.error("   ✗ Image generation timed out.");
    return;
  }
  console.log(`   ✓ Image generated by Leonardo: ${imageUrl}`);

  // Download and Upload to Supabase
  console.log("\n3. Uploading image to Supabase Storage...");
  const imgRes = await fetch(imageUrl);
  if (!imgRes.ok) {
    console.error("Failed to download image from Leonardo CDN.");
    return;
  }
  const arrayBuffer = await imgRes.arrayBuffer();
  const buffer = Buffer.from(arrayBuffer);

  const fileName = `${Date.now()}-chandrababu.jpg`;
  const filePath = `article-images/${fileName}`;

  const { error: uploadError } = await supabase.storage
    .from("news-images")
    .upload(filePath, buffer, {
      contentType: "image/jpeg",
      cacheControl: "3600",
      upsert: false
    });

  if (uploadError) {
    console.error("   ✗ Supabase upload error:", uploadError.message);
    return;
  }

  const { data: urlData } = supabase.storage.from("news-images").getPublicUrl(filePath);
  const publicImageUrl = urlData.publicUrl;
  console.log(`   ✓ Uploaded! Public URL: ${publicImageUrl}`);

  // Insert Post into Database
  console.log("\n4. Inserting article into database...");
  const slug = `chandrababu-best-cities-${Date.now()}`;
  const { error: insertError } = await supabase.from("blog_posts").insert({
    slug,
    title: "రాష్ట్రాలు వేరైనా తెలుగు జాతి ఒక్కటే.. హైదరాబాద్, అమరావతి ప్రపంచంలోనే బెస్ట్ సిటీస్ కావాలి",
    excerpt: summary,
    content: `## ${headline}\n\n${summary}\n\nహైదరాబాద్ మరియు అమరావతి నగరాలను అత్యున్నత ప్రమాణాలతో అభివృద్ధి చేసి ప్రపంచపటంలో ప్రత్యేక గుర్తింపు తీసుకువస్తామని ముఖ్యమంత్రి నారా చంద్రబాబు నాయుడు వెల్లడించారు.`,
    category: "andhra-pradesh",
    tags: ["andhra-pradesh", "politics", "chandrababu", "amaravati", "hyderabad"],
    meta_title: headline,
    meta_description: summary,
    og_image: publicImageUrl,
    author_name: "VarthaNow Auto Desk",
    language: "te",
    published: true,
    featured: true,
    reading_time_min: 3,
    published_at: new Date().toISOString()
  });

  if (insertError) {
    console.error("   ✗ Database insertion failed:", insertError.message);
  } else {
    console.log(`\n🎉 SUCCESS! Generated post successfully:`);
    console.log(`👉 COVER IMAGE URL: ${publicImageUrl}`);
  }
}

runGenerations().catch(console.error);
